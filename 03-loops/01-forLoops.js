// For Loops

for (var i = 0; i < 10; i++) {
    console.log(i)
}

for (var x = 0; x <= 20; x +=2) {
    console.log(x)
}

for (var a = 10; a >= 0; a -=1){
    console.log(a)
}

for (var x = 0; x >= -24; x -=2){
    console.log(x)
}

var str = 'Connor'

for (var i = 0; i< str.length; i++){
    console.log(str[i])
}

var sum = 0
for (var i = 1; i <= 50; i++){
    sum += i
}

